#include "libft.h"
#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	int	out;

	out = 0;
	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	else
	{
		if (n < 0)
		{
			ft_putchar_fd('-', fd);
			n *= -1;
		}
		if (n > 9)
		{
			out = n % 10;
			ft_putnbr_fd(n / 10, fd);
		}
		else
			out = n;
		ft_putchar_fd(out + '0', fd);
	}
}
