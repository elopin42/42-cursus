#include "printf.h"
#include <unistd.h>

void	ft_print_int(int c)
{
	char	s;

	if (c == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	else if (c < 0)
	{
		c *= -1;
		write(1, "-", 1);
	}
	while (c > 9)
	{
		s = c % 10 + '0';
		c /= 10;
		write(1, &s, 1);
	}
	s = c + '0';
	write(1, &s, 1);
}
